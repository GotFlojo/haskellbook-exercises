module Palindrome where

{-
Write a function that tells you wether a given string (or-
list) is a palindrome. Here you'll want to use a function called 'reverse'
a predefinded function that does just what it sounds like
-}

isPalindrom :: (Eq a) => [a] -> Bool
isPalindrom x = x == reverse x

main :: IO ()
main = print (isPalindrom "blah" )
